#include "../../includes/libft.h"
// vérifie si un entier est un caractère alphabetique

int	ft_isalpha(int c)
{
	if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		return (0);
	return (1);
}
